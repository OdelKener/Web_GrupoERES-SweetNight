document.addEventListener('DOMContentLoaded', function() {
    const phoneNumbers = document.querySelectorAll('.phone-number');
    const phoneMenus = document.querySelectorAll('.phone-menu');
    
    // Funci√≥n para cerrar todos los men√∫s
    function closeAllMenus() {
        phoneMenus.forEach(menu => {
            menu.classList.remove('show');
        });
    }

    // Mostrar/ocultar men√∫ al hacer click en el n√∫mero
    phoneNumbers.forEach((phone, index) => {
        phone.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            
            const menu = phoneMenus[index];
            if (!menu) return;
            
            // Si el men√∫ ya est√° abierto, cerrarlo
            if (menu.classList.contains('show')) {
                menu.classList.remove('show');
                return;
            }
            
            // Cerrar todos los men√∫s primero
            closeAllMenus();
            
            // Abrir el men√∫ correspondiente
            menu.classList.add('show');
        });
    });
    
    // Cerrar men√∫s al hacer click fuera
    document.addEventListener('click', function(e) {
        if (!e.target.closest('.phone-wrapper')) {
            closeAllMenus();
        }
    });
    
    // Cerrar men√∫s al hacer scroll
    window.addEventListener('scroll', function() {
        closeAllMenus();
    }, { passive: true });
    
    // Prevenir que el click en el men√∫ lo cierre
    phoneMenus.forEach(menu => {
        menu.addEventListener('click', function(e) {
            e.stopPropagation();
        });
    });
});

// ===== FUNCIONALIDAD DEL MEN√ö HAMBURGUESA CORREGIDA =====
const sidebar = document.getElementById('sidebar');
const toggleBtn = document.getElementById('toggleBtn');
const floatingMenuBtn = document.getElementById('floatingMenuBtn');

// Estado del men√∫ (true = abierto, false = cerrado)
let isMenuOpen = true;

// Cargar estado guardado
function loadMenuState() {
    const savedState = localStorage.getItem('menuState');
    if (savedState !== null) {
        isMenuOpen = savedState === 'true';
        updateMenuState();
    }
}

// Guardar estado
function saveMenuState() {
    localStorage.setItem('menuState', isMenuOpen.toString());
}

// Actualizar interfaz - VERSI√ìN SIMPLIFICADA
function updateMenuState() {
    if (isMenuOpen) {
        // Men√∫ ABIERTO
        sidebar.classList.remove('collapsed');
        document.body.classList.add('menu-open');
        document.body.classList.remove('menu-closed');
        toggleBtn.innerHTML = '‚Äπ';
        floatingMenuBtn.classList.add('hidden');
        
        // En m√≥viles, prevenir scroll del body
        if (window.innerWidth <= 768) {
            document.body.style.overflow = 'hidden';
        }
    } else {
        // Men√∫ CERRADO
        sidebar.classList.add('collapsed');
        document.body.classList.add('menu-closed');
        document.body.classList.remove('menu-open');
        toggleBtn.innerHTML = '‚Ä∫';
        floatingMenuBtn.classList.remove('hidden');
        
        // Restaurar scroll
        document.body.style.overflow = '';
    }
}

// Alternar men√∫
function toggleMenu() {
    isMenuOpen = !isMenuOpen;
    updateMenuState();
    saveMenuState();
}

// Event listeners
toggleBtn.addEventListener('click', function(e) {
    e.preventDefault();
    e.stopPropagation();
    toggleMenu();
});

floatingMenuBtn.addEventListener('click', function(e) {
    e.preventDefault();
    e.stopPropagation();
    toggleMenu();
});

// Cerrar men√∫ al hacer click fuera - SOLO EN M√ìVILES
document.addEventListener('click', function(event) {
    if (window.innerWidth <= 768 && isMenuOpen) {
        if (!sidebar.contains(event.target) && !floatingMenuBtn.contains(event.target)) {
            isMenuOpen = false;
            updateMenuState();
            saveMenuState();
        }
    }
});

// Cerrar men√∫ con tecla ESC
document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape' && isMenuOpen) {
        isMenuOpen = false;
        updateMenuState();
        saveMenuState();
    }
});

